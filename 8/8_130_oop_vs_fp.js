/* oop vs fp */

/*
****** OOP ******

- organizing code into units
- object contains information, the state
- operations on the state are methods


> objects are first class citizens
---> inheritance/polymorfism

* few operations on common data
# stateful
^ side effects
/ imperative

*/

/*
****** FP ******

- avoiding side effects w/ pure functions
- data is immutable
- code is a combination of functions
- a function cannot change the outside world

> HOF
> pure functions
> referential transparency
> functions are first class citizens
---> abstraction/encapsulation

* many operations to fix data
# stateless
^ pure functions
/ declarative

*/
